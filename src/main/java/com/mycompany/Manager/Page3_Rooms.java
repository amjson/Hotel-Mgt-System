    private void btnAddRoomActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddRoomActionPerformed
        // operation of createing a new account
        String room   = txtRoomNo.getText();
        String type   = txtRoomType.getSelectedItem().toString();
        String bed    = txtBed.getSelectedItem().toString();
        String price  = txtPrice.getText();
        String status = "Available";

        try {
            conn = DBConnection.setConnection();
            sqlQueryCheck = conn.prepareStatement("select * from tbl_rooms where room_no=? ");
            sqlQueryCheck.setString(1, room);
            res = sqlQueryCheck.executeQuery();

            if(res.isBeforeFirst()) {
                // if the credential already exist display error message
                JOptionPane.showMessageDialog(this, "There is a Room with this number already", "Please use new Room No", JOptionPane.ERROR_MESSAGE);

                // clearing neccessary fields to allow the admin to re-enter new Cridentials
                txtRoomNo.setText("");
                errorRoom.setText(null);
                if(txtRoomNo.getText().trim().equals("")){
                    txtRoomNo.setText("room no.(123)");
                    txtRoomNo.setForeground(Color.gray);
                    serialLine.setBackground(Color.black);
                }

                return;
            }else {
                sqlQueryInsert = conn.prepareStatement("insert into tbl_rooms (room_no,room_type,bed,price,status) values (?,?,?,?,?) ");
                sqlQueryInsert.setString(1, room);
                sqlQueryInsert.setString(2, type);
                sqlQueryInsert.setString(3, bed);
                sqlQueryInsert.setString(4, price);
                sqlQueryInsert.setString(5, status);

                //if the record is inserted successfully the state of executeUpdate() will be 1
                if (sqlQueryInsert.executeUpdate() != 0) {
                    JOptionPane.showMessageDialog(null, "A New Room has been added successfully.");

                    // clearing the form after successfully adding the new record in the database
                    txtRoomNo.setText("");
                    errorRoom.setText(null);
                    if(txtRoomNo.getText().trim().equals("")){
                        txtRoomNo.setText("room no.(123)");
                        txtRoomNo.setForeground(Color.gray);
                        serialLine.setBackground(Color.black);
                    }
                    
                    txtPrice.setText("");
                    if(txtPrice.getText().trim().equals("")){
                        txtPrice.setText("price(ksh)");
                        txtPrice.setForeground(Color.gray);
                        priceLine.setBackground(Color.black);
                    }

                    // refresh the page
                    new Page3_Rooms().setVisible(true);
                    this.dispose();

                    return;
                }
            }
        } catch (Exception ex) {
            Logger.getLogger(Page3_Rooms.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnAddRoomActionPerformed

