    private void btnSearchMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnSearchMouseClicked
        // function for searching specific record
        String RoomOrBed = txtForSearch.getText();

        try {
            conn = DBConnection.setConnection();
            sqlQueryCheck = conn.prepareStatement("select * from tbl_rooms where room_no like '%"+RoomOrBed+"%' or bed like '%"+RoomOrBed+"%' ");
            res = sqlQueryCheck.executeQuery();

            DefaultTableModel model = (DefaultTableModel)tbl_rooms.getModel();
            model.setRowCount(0);

            try {
                while(res.next()) {
                    model.addRow(new Object[]{
                        res.getString(1),
                        res.getString(2),
                        res.getString(3),
                        res.getString(4),
                        res.getString(5),
                        res.getString(6)
                    });
                }

                res.close();
            }
            catch (Exception e) {
                JOptionPane.showMessageDialog(null, e);
            }
        }
        catch (Exception ex) {
            Logger.getLogger(Page3_Rooms.class.getName()).log(Level.SEVERE,null, ex);
        }
    }//GEN-LAST:event_btnSearchMouseClicked

