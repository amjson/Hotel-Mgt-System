    private void btnDelMgActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDelMgActionPerformed
        // Operation for deleting manager account
        try {
            defaulttbl_2_delete = (DefaultTableModel)tbl_rooms.getModel();
            int selectIndex = tbl_rooms.getSelectedRow();

            int id = Integer.parseInt(defaulttbl_2_delete.getValueAt(selectIndex, 0).toString());

            // confirmation message
            int dialogResult = JOptionPane.showConfirmDialog(null, "Do you want to Delete this Room", "Warnimg", JOptionPane.YES_NO_OPTION);

            if(dialogResult == JOptionPane.YES_OPTION){
                try {
                    Connection conn = DBConnection.setConnection();
                    sqlQueryCheck = conn.prepareStatement("delete from tbl_rooms where id=?");
                    sqlQueryCheck.setInt(1, id);
                    sqlQueryCheck.executeUpdate();

                    JOptionPane.showMessageDialog(null, "The Room has been Deleted Successfully");

                    rooms_table();

                    // refreshing the current page
                    new Page3_Rooms().setVisible(true);
                    this.dispose();
                } catch (Exception ex) {
                    Logger.getLogger(Page3_Rooms.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "No Room has been selected!", "Warnimg", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnDelMgActionPerformed

